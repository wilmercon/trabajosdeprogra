def sumar_numeros():
    # Pedir al usuario dos números
    num1 = float(input("Ingrese el primer número: "))
    num2 = float(input("Ingrese el segundo número: "))
    
    # Calcular la suma
    suma = num1 + num2
    
    # Mostrar el resultado
    print(f"La suma de {num1} y {num2} es: {suma}")

# Llamar a la función
sumar_numeros()
